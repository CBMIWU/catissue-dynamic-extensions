package edu.common.dynamicextensions.xmi;

import java.util.List;


public class DynamicQueryList
{
	List<String> queryList;
	List<String> revQueryList;

	public List<String> getQueryList()
	{
		return queryList;
	}

	public void setQueryList(List<String> queryList)
	{
		this.queryList = queryList;
	}

	public List<String> getRevQueryList()
	{
		return revQueryList;
	}

	public void setRevQueryList(List<String> revQueryList)
	{
		this.revQueryList = revQueryList;
	}

}
